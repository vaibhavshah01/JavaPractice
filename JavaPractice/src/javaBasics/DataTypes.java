package javaBasics;

public class DataTypes 
{
	// shortcut to print on console - syso ctrl+spacebar
	// code about data types
	
	/* this is a practise session 
	 * which helps me boost my coding skill
	 */

	public static void main(String[] args) 
	{
		// primitive data types 
		// 1. int 
		int i = 10;
		i= 20; // now i value change from 10 to 20 where i is a variable.
		int j = 30;
		System.out.println(j);
		System.out.println(i+j);
		int k = -1;
		int l = 0;
		
		// 2. double 
		double d = 12.33;
		double d1 = 100; // 100.00
		
		// 3. char
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '%';
		
		// 4. boolean 
		boolean b1 = true; 
		boolean b2 = false;
		
		// Non primitive 
		// String  ( String is a class , not a data type )
		String s1 = "Ram";
		System.out.println(s1);
		String s2 = "100";
		String s3 = "12.33";
		System.out.println(s3);
		
	}

}
